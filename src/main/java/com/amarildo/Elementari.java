package com.amarildo;

public class Elementari {

    int somma(int a, int b){
        return a+b;
    }

    int sottrazione(int a, int b){
        return a-b;
    }

    int prodotto(int a, int b){
        return a*b;
    }

    int divisione(int a, int b){
        return a/b;
    }

    int tornaZero(){
        return 0;
    }

    int tornaUno(){
        return 1;
    }
}
